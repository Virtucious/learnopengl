#include <glad.h>
#include <glfw3.h>

#include "LibrariesThirdParty/glm/glm.hpp"
#include "LibrariesThirdParty/glm/gtc/matrix_transform.hpp"
#include "LibrariesThirdParty/glm/gtc/type_ptr.hpp"

#include "shader.h"
